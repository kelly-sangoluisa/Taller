    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = Tabla.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado el cliente que desea eliminar, por favor, seleccione el cliente que desea eliminar"); 
        }
        else{
            int resp = JOptionPane.showConfirmDialog(null, "多Esta seguro de eliminar el siguiente cliente?", "Alerta!", JOptionPane.YES_NO_OPTION);
            if(resp == JOptionPane.YES_OPTION){ 
                try { 
                    String sql = "delete from cliente where IdCliente="+idAux;
                    op.opcioness(sql);
                    JOptionPane.showMessageDialog(null, "Se ha eliminado el cliente");
                    jTFNombre.setText("");
                    jTFCedula.setText("");  
                    jTFPassword.setText("");
                   op.cargarTabla(modelo, Tabla, nombreTabla, Titulos, idTabla);
                }catch (Exception e){
                    JOptionPane.showMessageDialog(null, "Ocurrio un error al eliminar el cliente");
                } 
            }else {
                JOptionPane.showMessageDialog(null, "Eliminacion cancelada");
                jTFCedula.setText("");
                jTFNombre.setText("");
                jTFPassword.setText("");
            }   
        }
        Tabla.clearSelection();   
    }//GEN-LAST:event_btnEliminarActionPerformed

