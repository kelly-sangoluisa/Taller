    private void JBAgregarASActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBAgregarASActionPerformed
        int idSala = Integer.parseInt(jTFIdSala.getText());
        int idAsiento = Integer.parseInt(jTFIdAsiento.getText());
        boolean flag = true;
        if (cbmDisponibilidad.getSelectedIndex()<0||jTFIdSala.getText().equals("")||jTFIdAsiento.getText().equals("")) 
        {
            JOptionPane.showMessageDialog(null, "No se puede dejar campos vacios, por favor ingrese los datos");
        }
        else
        {  
            int resp = JOptionPane.showConfirmDialog(null, "Â¿Esta seguro de agregar ?", "Alerta!", JOptionPane.YES_NO_OPTION);          
            if(resp == JOptionPane.YES_OPTION)
            {
                for( int i = 0; i < TablaAsiento_Sala.getRowCount(); i++)
                {
                    int aux_idSala = (int) TablaAsiento_Sala.getValueAt(i, 1);
                    int aux_idAsiento = (int) TablaAsiento_Sala.getValueAt(i, 2);
                    if ((aux_idAsiento == idAsiento) && (aux_idSala == idSala))
                    {
                        flag = false;
                    }
                }
                if(flag)
                {
                    try 
                    {
                        String sql = "insert into asiento_sala (IdSalaA,IdAsientoS,Disponibilidad) values("+idSala+","+idAsiento+","+auxDisponibilidad+")";
                        op.opcioness(sql);
                        JOptionPane.showMessageDialog(null, "Se agrego con exitosamente");
                        jTFIdAsientoSala.setText("");
                        jTFIdSala.setText("");
                        jTFIdAsiento.setText("");
                        cbmDisponibilidad.setSelectedIndex(-1);
                        op.cargarTabla(modeloAsiento_Sala, TablaAsiento_Sala, nombreTablaAS, TitulosAS, idTablaAS);                      
                    }
                    catch(Exception ex)
                    {
                        JOptionPane.showMessageDialog(null,"Ocurrio un error al intentar agregar ");
                    }
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"No se puede agregar nuevamente la misma silla a la sala mas de una vez, agregacion cancelada");
                }

            }
            else
            {
                JOptionPane.showMessageDialog(null, "Agregracion cancelada");
            }
        }
        TablaAsiento.clearSelection();
        TablaSala.clearSelection();  
        jTFIdAsientoSala.setText("");
        jTFIdSala.setText("");
        jTFIdAsiento.setText("");
        cbmDisponibilidad.setSelectedIndex(-1);
    }//GEN-LAST:event_JBAgregarASActionPerformed

