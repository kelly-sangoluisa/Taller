    private void TablaSalaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaSalaMouseClicked
        int fila = TablaSala.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "No se ha seleccionado la fila");
        }else{
            pktablaSala =(int) TablaSala.getValueAt(fila, 0);
            String pk = TablaSala.getValueAt(fila, 0).toString();
            jTFIdSala.setText(pk);
            
            idSala = (int) TablaSala.getValueAt(fila, 0);
            formato = (String)TablaSala.getValueAt(fila, 1);
            jTFSala.setText(TablaSala.getValueAt(fila, 0).toString());
            switch (formato) {
                case "Sala 2D":
                    cbmFormato.setSelectedIndex(0);
                    break;
                case "Sala 4D":
                    cbmFormato.setSelectedIndex(1);
                    break;
                case "Sala VIP":
                    cbmFormato.setSelectedIndex(2);
                    break;
                default:
                    break;
            }
        }
        
    }//GEN-LAST:event_TablaSalaMouseClicked

