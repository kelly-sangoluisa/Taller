    public void validadorNumero (java.awt.event.KeyEvent evt){
         char variable = evt.getKeyChar();
        if (Character.isLetter(variable)){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Ingrese solo numeros ","Error",JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void validadorLetras (java.awt.event.KeyEvent evt){
         char variable = evt.getKeyChar();
        if (Character.isDigit(variable)){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Ingrese solo letras ","Error",JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void btnCrearCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearCuentaActionPerformed
       try{
        String IdCliente = jTFCedula.getText();
        String Nombre = jTFNombre.getText();
        String Contrasenia = jTFPassword.getText();
        int id = Integer.parseInt(IdCliente);
        
        if (IdCliente.equals("") || Nombre.equals("") || Contrasenia.equals("")) {
            JOptionPane.showMessageDialog(null, "No se puede dejar campos vacios, por favor ingrese los datos");
        } else {
            if (opl.validarIdUnico(id)) {
                int resp = JOptionPane.showConfirmDialog(null, "¬øEsta seguro de crear una nueva cuenta?", "Alerta!", JOptionPane.YES_NO_OPTION);
                if (resp == JOptionPane.YES_OPTION) {
                    String sql = "insert into cliente(IdCliente, Nombre,Contrase√±a)values ("+id+",'"+Nombre+"','"+Contrasenia+"')";
                    try {
                        op.opcioness(sql);
                        JOptionPane.showMessageDialog(null, "Se creo exitosamente su cuenta.\n    Porfavor inicie sesi√≥n    ");
                        jTFNombre.setText("");
                        jTFCedula.setText("");
                        jTFPassword.setText("");
                        this.setVisible(false);
                        JFLogin l = new JFLogin();
                        l.setVisible(true);
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, "Ocurrio un error al intentar agregar un nuevo Cliente");
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "El id ingresado ya esta usado por otro cliente\n       Porfavor ingrese un numero valido      ");
                jTFNombre.setText("");
                jTFCedula.setText("");
                jTFPassword.setText("");
            }
        }
        }catch(NumberFormatException e){
        JOptionPane.showMessageDialog(null, "Complete todos los datos");
        }
    }//GEN-LAST:event_btnCrearCuentaActionPerformed

